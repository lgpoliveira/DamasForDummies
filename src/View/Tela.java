    public void removerPecas (int lin,int col)
    {
        ImgPanel painel = getPanel(lin, col);
        painel.setImg(casaVazia);
    }
    public void updatePecas(int lin, int col) {
        ImgPanel painel = getPanel(lin, col);

        switch (countClick) {
            case 0:
            {
               if (Tabuleiro.getPeca().getCor() == 0) {
                    painel.setImg(pecaPretaSelecionada);
                } else
                    painel.setImg(pecaBrancaSelecionada);

                linha = lin;
                coluna = col;
                painel.repaint();
                panelTemp = painel;
                countClick++;
                break;
            }
            case 1:
            {
               if (Tabuleiro.getPeca().getCor() == 0) {
                        painel.setImg(pecaPreta);
                    } else if (Tabuleiro.getPeca().getCor() == 1) {
                        painel.setImg(pecaBranca);
                    }
                    panelTemp.setImg(casaVazia);
                    panelTemp.repaint();
                    painel.repaint();
                    countClick = 0;
            }

    }
    }
