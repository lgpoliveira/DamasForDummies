    public void updateImgPanel(int line, int col) {
        ImgPanel painel = getPanel(line, col);
        switch (countClick) {
            case 0:
                if (jogo.getCasa(line, col).getPeca() != null && jogo.getCasa(line, col).getPeca().getCor() == 0) {
                    painel.setImg(pecaPretaSelecionada);
                } else if (jogo.getCasa(line, col).getPeca() != null && jogo.getCasa(line, col).getPeca().getCor() == 1) {
                    painel.setImg(pecaBrancaSelecionada);
                } else if (jogo.getCasa(line, col).getPeca() == null && painel.getImg() != casaVazia) {
                    // O código só entra nesse if quando uma peça é comida.
                    painel.setImg(casaVazia);
                    painel.repaint();
                    break;
                }
                linha = line;
                coluna = col;
                painel.repaint();
                panelTemp = painel;
                break;
            case 1:

                if (jogo.getCasa(line, col).getPeca() == null) {
                    if (jogo.getCasa(linha, coluna).getPeca() != null && jogo.getCasa(linha, coluna).getPeca().getCor() == 0) {
                        painel.setImg(pecaPreta);
                    } else if (jogo.getCasa(linha, coluna).getPeca() != null && jogo.getCasa(linha, coluna).getPeca().getCor() == 1) {
                        painel.setImg(pecaBranca);
                    }
                    panelTemp.setImg(casaVazia);
                    panelTemp.repaint();
                    painel.repaint();
                   
                    jogo.getCasa(line, col).setPeca(jogo.getCasa(linha, coluna).getPeca());
                    jogo.getCasa(linha, coluna).setPeca(null);
                    break;
                } else {
                    if (jogo.getCasa(linha, coluna).getPeca() != null && jogo.getCasa(linha, coluna).getPeca().getCor() == 0) {
                        panelTemp.setImg(pecaPreta);
                    } else if (jogo.getCasa(linha, coluna).getPeca() != null && jogo.getCasa(linha, coluna).getPeca().getCor() == 1) {
                        panelTemp.setImg(pecaBranca);
                    }
                    panelTemp.repaint();
                    
                    try {
                        Thread.sleep(500);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(Tela.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
        }

    }

